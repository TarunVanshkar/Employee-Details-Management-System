    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String empID,salary,phone,team,fname,lname,city;
            empID=EmpID.getText();
            fname=Fname.getText();   // Fname is a varaible name of text field of first name
            lname=Lname.getText();
            team=Team.getText();
            city=City.getText();
            phone=Phone.getText();
            salary=Salary.getText();
            
            
            pre=con.prepareStatement("insert into employee_record(empID,fname,lname,salary,phone,city,team)values(?,?,?,?,?,?,?)");
            pre.setString(1,empID);
            pre.setString(2,fname);
            pre.setString(3,lname);
            pre.setString(4,salary);
            pre.setString(5,phone);
            pre.setString(6,city);
            pre.setString(7,team);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(this,"Details Saved");
            Fname.setText("");
            Lname.setText("");
            City.setText("");
            Phone.setText("");
            Salary.setText("");
            EmpID.setText("");
            Team.setText("");
            
            EmpID.requestFocus();
            update_table();
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

