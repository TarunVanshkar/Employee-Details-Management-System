    public void update_table()
    {
        int cc;
        try {
            pre=con.prepareStatement("select * from employee_record");
            
            ResultSet rs=pre.executeQuery();
            ResultSetMetaData RSMD=rs.getMetaData();
            cc=RSMD.getColumnCount();
            DefaultTableModel DFT=(DefaultTableModel) jTable1.getModel();  //object
            DFT.setRowCount(0);
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int i=1; i<=cc; i++)
                {
                    v2.add(rs.getString("EmpID"));
                    v2.add(rs.getString("Fname"));
                    v2.add(rs.getString("Lname"));
                    v2.add(rs.getString("Salary"));
                    v2.add(rs.getString("Phone"));
                    v2.add(rs.getString("City"));
                    v2.add(rs.getString("Team"));
                }
                DFT.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

